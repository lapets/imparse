def bnfTokenize(bnfFile):
  bnf = open(bnfFile)

  termRes = '\\\"[' + reserved + ']\\\"' # Reserved characters as terminals
  termNT = '::=|[^\s' + reserved + ']+' # Terminals and nonterminals

  regex = '|'.join([\
    comment,\
    regExpr,\
    ('[' + reserved + ']'),\
    termRes,\
    termNT,\
    ])

  regex = re.compile(regex)

  tmp = []
  for line in bnf:
    tmp = tmp + regex.findall(line)
  tokens = [t for t in tmp if not (t == None or t.isspace() or t == '')]
  bnf.close()
#  print(tokens)
  return tokens


def toUxadt(ptree):
  # Unwrap
  ps = ptree['Grammar']
  ps2 = []

  for p in ps:
    prodId = p['Production'][0]
    cs = p['Production'][1:]
    cb = []
    for c in cs:
      (label, seq) = ('', [])
      for i in range(len(c['Choice'])):
        if i == 0:
          label = c['Choice'][i]
          if label == 'None':
            label = None
        else:
          es = c['Choice'][i]
          r = toUxadtExpr(es)
          if r is not None:
            seq = seq + r
          else: break
      cb = cb + [Choice(label, AssocNone(), seq)]
    ps2 = ps2 + [Production(prodId, [Choices(cb)])]
  return Grammar(ps2)

def toUxadtExpr(es):
  if type(es) == str: 
    return [Terminal(es)]

  cs = []
  ty = list(es.keys())[0]
  x = es[ty][0]

  if ty == 'Comment':
    while x[0] == '#' or x[0].isspace():
      x = x[1:]
    if x[-1] == '\n':
      x = x[:-1]
    cs = cs + [Comment(x)]
  elif ty == 'Terminal':
    if len(x) == 3 and x[0] == '\"' and x[-1] == '\"':
      x = x[1]
    cs = cs + [Terminal(x)]
  elif ty == 'Nonterminal':
    cs = cs + [Nonterminal(x[1:])]
  elif ty == 'Empty String':
    cs = cs + [Terminal('\"\"')]
  elif ty == 'RegExpr':
    s = '/' + x[1:-1] + '/'
    cs = cs + [RegExpr(s)]
  # One/May/Many/MayMany
  elif ty == 'One':
    cs = cs + [One(toUxadtExpr(x))]
  elif ty == 'May':
    m = []
    for e in es[ty]:
      m = m + toUxadtExpr(e)
    cs = cs + [May(m)]
  elif ty == 'Many':
    m = []
    for e in es[ty]:
      m = m + toUxadtExpr(e)
    cs = cs + [Many(m)]
  elif ty == 'MayMany':
    m = []
    for e in es[ty]:
      m = m + toUxadtExpr(e)
    cs = cs + [MayMany(m)]
  # Group
  elif ty == 'Group':
    for e in es[ty]:
      cs = cs + toUxadtExpr(e)

  return cs


