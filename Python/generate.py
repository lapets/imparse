# Writes a UxADT grammar to a file
def writeUxadt(u, name = 'gen', indent = 2):
  s = uxadtToStr(u, indent)
  f = open(name + '.py', 'w')
  f.write(
'''
##########################################
##
##  UxADT grammar generated by Imparse.
##
##########################################

'''
)
  f.write(name + 'Uxadt = ')
  f.write(s)
  f.close()

def uxadtToStr(g, indent = 2):
  indent = ' ' * indent
  ps = g.match(Grammar(_), lambda ps: ps).end
  st = 'Grammar([\\'
  for p in ps:
    (nt, cbs) = p.match(Production(_, _), lambda nt, cbs: (nt, cbs)).end
    st = st + '\n' + indent + 'Production(\'' + nt + '\', [\\'
    for cb in cbs:
      cs = cb.match(Choices(_), lambda cs: cs).end
      st = st + '\n' + (indent * 2) + 'Choices([\\'
      for c in cs:
        (label, seq) = c.match(Choice(_, _, _), lambda l, a, seq: (l, seq)).end
        if label is None:
          st = st + '\n' + (indent * 3) + 'Choice(None, AssocNone(), [\\'
        else:
          st = st + '\n' + (indent * 3) + 'Choice(\'' + label + '\', AssocNone(), [\\'
        r = uxadtExprToStr(seq)
        st = st + '\n' + (indent * 4) + r + '\\\n' + (indent * 4) +']),\\'
      st = st + '\n' + (indent * 3) + ']),\\'
    st = st + '\n' + (indent * 2) + ']),\\'
  st = st + '\n'+ indent + '])'
  return st

def uxadtExprToStr(seq):
  s = ''
  for x in seq:
    et = etype(x)
    (ty, expr) = et
 
    if ty == 'One':
      r = uxadtExprToStr(expr)
      s = s + 'One([' + r + ']), '
    elif ty == 'May':
      r = uxadtExprToStr(expr)
      s = s + 'May([' + r + ']), '
    elif ty == 'Many':
      r = uxadtExprToStr(expr)
      s = s + 'Many([' + r + ']), '
    elif ty == 'MayMany':
      r = uxadtExprToStr(expr)
      s = s + 'MayMany([' + r + ']), '
    elif ty == 'Terminal':
      s = s + 'Terminal(\'' + expr + '\'), '
    elif ty == 'RegExpr':
      s = s + 'RegExpr(\'' + expr + '\'), '
    elif ty == 'Nonterminal':
      s = s + 'Nonterminal(\'' + expr + '\'), '
  return s
  

